// --------------------------------------------------------------------------
//! Constructor
// --------------------------------------------------------------------------
CARD::CARD( const SUIT _suit, const int _value ) : m_suit( _suit ), m_value( _value )
{
}

// --------------------------------------------------------------------------
//! Returns the suit of the card
// --------------------------------------------------------------------------
SUIT CARD::get_suit() const
{
	return m_suit;
}

// --------------------------------------------------------------------------
//! Returns the value of the card
// --------------------------------------------------------------------------
int CARD::get_value() const
{
	return m_value;
}
